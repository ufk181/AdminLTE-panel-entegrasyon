    //--------------------------------------------------------------------------------------------------
    // Null Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function null( & $var, $value)
    {
        $var = $var ?? $value;
    }

    //--------------------------------------------------------------------------------------------------
    // False Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function false( & $var, $value)
    {
        $var = $var === false ? $value : $var;
    }

    //--------------------------------------------------------------------------------------------------
    // Empty Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function empty( & $var, $value)
    {
        $var = empty($var) ? $value : $var;
    }
}
