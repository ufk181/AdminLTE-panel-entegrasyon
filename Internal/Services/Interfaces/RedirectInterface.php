    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------
    // redirect() -> 5.1.0
    //--------------------------------------------------------------------------------------------------
    //
    // @param string $url
    // @param int    $time
    // @param array  $data
    // @param bool   $exit
    //
    //--------------------------------------------------------------------------------------------------
    public function location(String $url = NULL, Int $time = 0, Array $data = NULL, Bool $exit = true);

    //--------------------------------------------------------------------------------------------------
    // redirectData() -> 5.1.0
    //--------------------------------------------------------------------------------------------------
    //
    // @param string $k
    //
    // @return mixed
    //
    //--------------------------------------------------------------------------------------------------
    public function selectData(String $k);

    //--------------------------------------------------------------------------------------------------
    // redirectDeleteData() -> 5.1.0
    //--------------------------------------------------------------------------------------------------
    //
    // @param mixed $data
    //
    // @return bool
    //
    //--------------------------------------------------------------------------------------------------
    public function deleteData($data) : Bool;

    //--------------------------------------------------------------------------------------------------------
    // Status -> 4.3.5
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function status() : Int;

    //--------------------------------------------------------------------------------------------------------
    // Url -> 4.3.5
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function url() : String;

    //--------------------------------------------------------------------------------------------------------
    // Query String -> 4.3.5
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function queryString() : String;

    //--------------------------------------------------------------------------------------------------------
    // action()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string $action
    //
    //--------------------------------------------------------------------------------------------------------
    public function action(String $action = NULL);

    //--------------------------------------------------------------------------------------------------------
    // time()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var numeric $time
    //
    //--------------------------------------------------------------------------------------------------------
    public function time(Int $time = 0) : Redirect;

    //--------------------------------------------------------------------------------------------------------
    // wait()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var numeric $time
    //
    //--------------------------------------------------------------------------------------------------------
    public function wait(Int $time = 0) : Redirect;

    //--------------------------------------------------------------------------------------------------------
    // data()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var array $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function data(Array $data) : Redirect;

    //--------------------------------------------------------------------------------------------------------
    // insert()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var array $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function insert(Array $data) : Redirect;

    //--------------------------------------------------------------------------------------------------------
    // select()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string $key
    //
    //--------------------------------------------------------------------------------------------------------
    public function select(String $key);

    //--------------------------------------------------------------------------------------------------------
    // delete()
    //--------------------------------------------------------------------------------------------------------
    //
    // @var mixed $key
    //
    //--------------------------------------------------------------------------------------------------------
    public function delete($key) : Bool;
}
