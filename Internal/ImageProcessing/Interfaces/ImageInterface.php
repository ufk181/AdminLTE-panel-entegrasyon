<?php namespace ZN\ImageProcessing;

use stdClass;

interface ImageInterface
{
    //--------------------------------------------------------------------------------------------------------
    // Get Prosize
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $path
    // @param int    $width
    // @param int    $height
    //
    //--------------------------------------------------------------------------------------------------------
    public function getProsize(String $path, Int $width = 0, Int $height = 0) : stdClass;

    //--------------------------------------------------------------------------------------------------------
    // Thumb
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $fpath
    // @param array  $set
    //
    //--------------------------------------------------------------------------------------------------------
    public function thumb(String $fpath, Array $set) : String;
}
